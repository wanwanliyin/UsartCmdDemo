void Uart1Init(u32 bound)
{
    GPIO_InitTypeDef GPIO_InitStructure;
    USART_InitTypeDef USART_InitStructure;
    NVIC_InitTypeDef NVIC_InitStructure;
     
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_USART1|RCC_APB2Periph_GPIOA, ENABLE);	//使能USART1，GPIOA时钟
    
    //USART1_TX   GPIOA.9
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9; //PA.9
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出
    GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.9
    
    //USART1_RX	  GPIOA.10初始化
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;//PA10
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入
    GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.10  
    
    //Usart1 NVIC 配置
    NVIC_InitStructure.NVIC_IRQChannel = USART1_IRQn;
    NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=3 ;//抢占优先级3
    NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3;		//子优先级3
    NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能
    NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器
    
    //USART 初始化设置
    
    USART_InitStructure.USART_BaudRate = bound;//串口波特率
    USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
    USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
    USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
    USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制
    USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式
    
    USART_Init(USART1, &USART_InitStructure); //初始化串口1
    USART_ITConfig(USART1, USART_IT_RXNE, DISABLE);//开启串口接受中断
    USART_ITConfig(USART1,USART_IT_IDLE,ENABLE);   //开启串口空闲中断
    USART_Cmd(USART1, ENABLE);                    //使能串口1 

}


extern TaskHandle_t UsartTask_Handler; //任务句柄

void USART1_IRQHandler(void)         
{
	u8 res;	    
    BaseType_t pxHigherPriorityTaskWoken;
	if(USART_GetITStatus(USART1, USART_IT_IDLE) != RESET)
	{	 	
        if(UsartTask_Handler != NULL)
        {
           vTaskNotifyGiveFromISR(UsartTask_Handler, &pxHigherPriorityTaskWoken);
        }
        res=USART1->SR;  
        res =USART1->DR;

	} 

} 

void Uart2Init(u32 bound)
{
	//GPIO端口设置
	GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;

  		 
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART2, ENABLE);	//使能USART1，GPIOA时钟
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
	//USART1_TX   GPIOA.9
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2; 
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出
	GPIO_Init(GPIOA, &GPIO_InitStructure);

	//USART1_RX	  GPIOA.10初始化
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_3;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入
	GPIO_Init(GPIOA, &GPIO_InitStructure);

	//Usart1 NVIC 配置
	NVIC_InitStructure.NVIC_IRQChannel = USART2_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=3 ;//抢占优先级3
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3;		//子优先级3
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器
	
		//USART1 初始化设置
	USART_InitStructure.USART_BaudRate = bound;//串口波特率
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式

	USART_Init(USART2, &USART_InitStructure); 
	USART_ITConfig(USART2, USART_IT_RXNE, DISABLE);
	USART_Cmd(USART2, ENABLE);                    
	
 
}



static u16 s_dmaReadPt = 0;

u16 UartCheckData(void)
{
    u16 pro;
    pro = DMA_RCVBUF_SIZE - DMA_GetCurrDataCounter(DMA1_Channel5);
    if(s_dmaReadPt != pro)
    {
        if (s_dmaReadPt <= pro)
        {
            return pro -s_dmaReadPt;
        }
        else
        {
			return pro + DMA_RCVBUF_SIZE - s_dmaReadPt;
        }
    }
    return 0;
}

u16 UartRxData(u8 *pDataBuf, u16 usLen)
{
    u16 WritePt;
    u16 Len = 0;
    WritePt = DMA_RCVBUF_SIZE - DMA_GetCurrDataCounter(DMA1_Channel5);

 //   printf("WritePt = %d\r\n",WritePt);
    if (WritePt >= DMA_RCVBUF_SIZE)   //环形DMA模式，实际大于DMA_RCVBUF_SIZE就会丢包
    {
        WritePt = 0;
    }
    
	while (usLen--)
	{
		if (s_dmaReadPt != WritePt)
		{
            *pDataBuf++ = g_DmaRcvBuf[s_dmaReadPt++];
            s_dmaReadPt %= DMA_RCVBUF_SIZE;
            Len++;
        }
        else
        {
            break;
        }
	}
	
    return Len;
}



